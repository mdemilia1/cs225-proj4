    public Expense createExpense()
    {
        Expense e = new Expense();
        if(amountField.getText().trim().length() > 0){
            e.setValue(new Double(amountField.getText()));
        }
        else{
            e.setValue(0.0);
        }
        e.setDescription(descriptionField.getText());
        return e;
    }
    

