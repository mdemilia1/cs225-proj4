    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        Expense e = createExpense();
        
        if(e.getDescription().trim().isEmpty()){
            JOptionPane.showMessageDialog(
                    null, "Expense must have a description.", "No Expense Description", JOptionPane.ERROR_MESSAGE);
        }
        
        else if(e.getValue() == 0.0){
            JOptionPane.showMessageDialog(
                    null, "Expense must have a value.", "No Expense Value", JOptionPane.ERROR_MESSAGE);
         }
        
        else{
            confirm = true;
            this.dispose();
        }
    }//GEN-LAST:event_saveButtonActionPerformed

